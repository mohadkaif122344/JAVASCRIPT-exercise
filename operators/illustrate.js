//Illustration of (!=) operator
let x = 5;
let y = '5';

console.log(x != 6);
console.log(y != '5');        
console.log(x != y);
console.log();

console.log(0 != false);   
console.log(0 != null);
console.log(NaN != NaN);




// Illustration of (===) operator
let x = 5;
let y = '5';

console.log(x === 6);
console.log(y === '5');        
console.log(x === y);
console.log();

console.log(NaN === NaN);   
console.log(0 === false);   
console.log(0 === null);





// Illustration of (!==) operator
let x = 5;
let y = '5';

console.log(x !== 6);
console.log(y !== '5');        
console.log(x !== y);
console.log();

console.log(0 !== false);   
console.log(0 !== null);
console.log(NaN !== NaN);





